# practice of python collections

# list example (ordered, changeable)
fruits = ["apple", "banana", "mango"]
print("Fruits list:", fruits)

# set example (no duplicates)
numbers = {1, 2, 3, 3, 4}
print("Unique numbers:", numbers)

# dictionary example (key-value pairs)
student = {
    "name": "Sai",
    "age": 24,
    "city": "Texas"
}

print("Student name:", student["name"])
print("Student city:", student["city"])
